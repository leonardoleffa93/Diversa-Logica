<?php

    /*
    a definição de uma variavel para repetição pode ser definida nas configurações da mesma
    no código é definido a variável "i" como igual a "0"
    O bloco de comando irá repetir enquanto o $i < 10
    Irá iniciar com 0
    Repetirá 10 vezes (De 0 a 9)
    */

    for ($i=0; $i < 10; $i++) {
        
        echo $i."\n";
    }
    
?>
